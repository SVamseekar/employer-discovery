    companies = load_static_companies()
    by_country = {}
    for c in companies:
        country = c["Country"]
        by_country.setdefault(country, 0)
        by_country[country] += 1

    print(f"\nStatic company list: {len(companies)} total\n")
    print("By country:")
    for country, count in sorted(by_country.items(), key=lambda x: -x[1]):
        print(f"  {country:<25} {count}")
    print(f"\nSectors breakdown: {len(set(c['Sector'] for c in companies))} unique sectors")
    print(f"EU Blue Card eligible: {sum(1 for c in companies if c['Region_Eligibility'] == 'Eligible')}")
