def load_static_companies():
    """
    Return all static companies as a list of dicts matching FIELDS in schema.py.
    Deduped by company name (case-insensitive) before returning.
    """
    all_companies = (
        US_BIG_TECH +
        US_AI_DATA +
        EU_MNCS +
        EU_TECH +
        INDIA_EMPLOYERS +
        JAPAN_EMPLOYERS +
        AU_NZ_EMPLOYERS +
        GLOBAL_REMOTE
    )

    # Deduplicate by lowercase company name
    seen = set()
    unique = []
    for co in all_companies:
        key = co["Company"].lower().strip()
        if key not in seen:
            seen.add(key)
            unique.append(co)

    return unique


